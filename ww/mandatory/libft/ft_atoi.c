int	ft_atoi(const char *str)
{
	long int	n;
	int			s;

	n = 0;
	s = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == '-')
	{
		s = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
		n = (n * 10) + *(str++) - 48;
	return ((int)(n * s));
}
